#include "main.h"

/**
 * print_triangle - draws a triangle on the terminal with '#'
 * @size: number of lines '#' should be repeated
 * Return - void
 */
void print_triangle(int size)
{
	int i, j;

	for (i = 0; i < size; i++)
	{
		for (j = 0; j < size - i - 1; j++)
		{
			_putchar(' ');
		}
		for (j = 0; j <= i; j++)
		{
			_putchar('#');
		}
		_putchar('\n');
	}
}
